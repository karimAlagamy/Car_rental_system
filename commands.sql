-- Create the database
CREATE DATABASE Car_rental_system;

USE Car_rental_system;

-- Create office table
CREATE TABLE office (
    office_id INT AUTO_INCREMENT PRIMARY KEY,
    office_name VARCHAR(100) NOT NULL,
    location VARCHAR(255) NOT NULL
);

-- Create users table
CREATE TABLE `user` (
    user_id INT AUTO_INCREMENT PRIMARY KEY,
    first_name VARCHAR(50) NOT NULL,
    last_name VARCHAR(50) NOT NULL,
    email VARCHAR(100) UNIQUE NOT NULL,
    phone_number VARCHAR(15) NOT NULL,
    address VARCHAR(255) NOT NULL,
    username VARCHAR(50) UNIQUE NOT NULL,
    password VARCHAR(255) NOT NULL,
    user_type CHAR(1) NOT NULL DEFAULT 'C',
    CONSTRAINT chk_user_type CHECK (user_type IN ('C', 'A'))
);

-- Create car table
CREATE TABLE car (
    car_id INT AUTO_INCREMENT PRIMARY KEY,
    make VARCHAR(100) NOT NULL,
    model VARCHAR(100) NOT NULL,
    no_of_seats INT NOT NULL,
    year INT NOT NULL,
    plate_number VARCHAR(20) UNIQUE NOT NULL,
    status VARCHAR(50) NOT NULL,
    office_id INT NULL,
    day_rate DECIMAL(10, 2) NOT NULL,
    last_update_date DATE DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT chk_car_status CHECK (status IN ('Active', 'Out of Service', 'Rented'))
);

-- Create reservation table
CREATE TABLE reservation (
    reservation_id INT AUTO_INCREMENT PRIMARY KEY,
    user_id INT NOT NULL,
    car_id INT NOT NULL,
    reservation_date DATE NOT NULL,
    pickup_date DATE NOT NULL,
    return_date DATE NOT NULL,
    total_amount DECIMAL(10, 2) NOT NULL,
    CONSTRAINT chk_reservation_dates CHECK (
        reservation_date <= pickup_date
        AND pickup_date < return_date
    )
);

